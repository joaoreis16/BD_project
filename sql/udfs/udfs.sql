/*
*	RETORNA 0 SE EQUIPAMENTO JA ESTA A SER UTILIZADO
*	RETORNA 1 SE ESTIVER LIVRE
*/
CREATE FUNCTION EXERCITO.equipIsAvailable (@id INT)
RETURNS BIT
AS
BEGIN
	IF EXISTS (SELECT * FROM EXERCITO.utiliza_equipamento WHERE equipamento=@id)
		RETURN 0
	RETURN 1
END